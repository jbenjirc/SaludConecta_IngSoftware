     private void cargarNombreCompleto() {
        Connection con = null;
        PreparedStatement pst = null;
        ResultSet rs = null;

        try {
            con = CONEXION.obtenerConexion();
            if (con != null) {
                String sql = "SELECT empleado_nombre + ' ' + empleado_paterno + ' ' + empleado_materno AS nombre_completo " +
                             "FROM Empleado " +
                             "WHERE id_empleado = ?";
                pst = con.prepareStatement(sql);
                pst.setInt(1,3); // Suponiendo que idUsuario es igual a id_empleado

                rs = pst.executeQuery();
                if (rs.next()) {
                    String nombreCompleto = rs.getString("nombre_completo");
                    txtpaciente.setText(nombreCompleto); // Mostrar el nombre completo en txtPaciente
                } else {
                    JOptionPane.showMessageDialog(null, "Empleado no encontrado.");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Error al conectar con la base de datos.");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al cargar el nombre del empleado: " + e.getMessage());
        } finally {
            try {
                if (rs != null) rs.close();
                if (pst != null) pst.close();
                if (con != null) con.close();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error al cerrar la conexi√≥n: " + e.getMessage());
            }
        }
    }
      
